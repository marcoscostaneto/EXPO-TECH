                        <p>&emsp;Python é uma linguagem de programação simples e versátil, pois, como sua sintaxe é moderna e objetiva, ela foi elaborada para que os desenvolvedores escrevam instruções com menos linhas de código. Seu densevolvimento começo no final da década de 1980 por Guido Van Rossum, enquanto trabalhava no Centrum Wiskunde & Informatica (CWI) em Amsterdã. Com o objetivo de criar uma linguagem de script mais eficiente, que seria capaz de acessar recursos do sistema operacional Amoeba. Mas somente em 1994, surgiu a primeira versão a linguagem Python, desde de então a linguagem recebeu diversas <a href="Bloco.php"  class="blocoLink">atualizações</a>, como uma linguagem de código aberto sua utilização e gratuita.</p>

